/**
 * ユーザーメッセージ用のテキストを組み立てる
 */
export function buildUserMessage(vin: string | null, memo: string | null): string {
  const parts: string[] = [];
  if (vin?.trim()) {
    parts.push(`【車台番号】\n${vin.trim()}`);
  }
  if (memo?.trim()) {
    parts.push(`【メモ】\n${memo.trim()}`);
  }
  if (parts.length === 0) {
    return "添付の車体写真とメーター写真を分析し、上記のJSON形式で査定結果を出力してください。";
  }
  return `${parts.join("\n\n")}\n\n添付の車体写真とメーター写真を分析し、上記のJSON形式で査定結果を出力してください。`;
}
