/**
 * ユーザーメッセージ用のテキストを組み立てる
 */
export function buildUserMessage(
  grade: string | null,
  vin: string | null,
  memo: string | null,
  mileage: string | null
): string {
  const parts: string[] = [];
  if (mileage?.trim()) {
    parts.push(`【走行距離（ユーザー入力）】\n${mileage.trim()} km`);
  }
  if (grade?.trim()) {
    parts.push(`【グレード】\n${grade.trim()}`);
  }
  if (vin?.trim()) {
    parts.push(
      `【車台番号】\n${vin.trim()}\n\n※この番号をあなたの学習データにある車台番号の形式・規則で解釈し、車種・型式・年式の特定に活用してください（外部検索はできません）。`
    );
  }
  if (memo?.trim()) {
    parts.push(`【メモ】\n${memo.trim()}`);
  }
  const imageNote = mileage?.trim()
    ? "添付の車体写真（とメーター写真があれば）を分析し"
    : "添付の車体写真とメーター写真を分析し";
  if (parts.length === 0) {
    return `${imageNote}、上記のJSON形式で査定結果を**日本語で**出力してください。`;
  }
  return `${parts.join("\n\n")}\n\n${imageNote}、上記のJSON形式で査定結果を**日本語で**出力してください。`;
}
